    let documentMenu, documentList;

    function clearPage(documentMenu, documentList) {
        documentMenu.innerHTML = documentList.innerHTML = "";
    }

    function changePage(documentMenu, documentList, Documents) {
        documentMenu = document.querySelector(".documents-menu");
        documentList = document.querySelector(".documents-list");
        clearPage(documentMenu, documentList);
        for (let i = 0; i < Documents.length; i++) {
            if (i == 0) {
                documentMenu.innerHTML += `<a href=${Documents[i].id} class="documents-link active">${Documents[i].name}</a>`;
            } else {
                documentMenu.innerHTML += `<a href=${Documents[i].id} class="documents-link">${Documents[i].name}</a>`;
            }
            let listCoursesItemHTML = "";
            for (let j = 0; j < Documents[i].documentsList.length; j++) {
                listCoursesItemHTML +=
                    `<li class="list-courses-item"><a class="list-courses-link" href=${
                        Documents[i].documentsList[j].url
                    } target="blank">${j + 1}` +
                    ". " +
                    `${Documents[i].documentsList[j].name}</a></li>`;
            }
            documentList.innerHTML +=
                `<div class="document-subject">
		<h3 class="document-title" id=${Documents[i].id.slice(1)}>${
                    Documents[i].name
                }</h3>
		<ul class="list-courses">` +
                listCoursesItemHTML +
                `</ul></div>`;
        }
        let documentLinks, documentSubjects;
        if (
            document.querySelectorAll(".documents-link") != null &&
            document.querySelectorAll(".document-subject") != null
        ) {
            documentLinks = document.querySelectorAll(".documents-link");
            documentSubjects = document.querySelectorAll(".document-subject");

            for (let i = 0; i < documentSubjects.length; i++) {
                if (documentLinks[i] != null) {
                    documentLinks[i].addEventListener("click", function () {
                        documentLinks[i].classList.add("active");
                        for (let j = 0; j < documentLinks.length; j++) {
                            if (j != i) {
                                documentLinks[j].classList.remove("active");
                            }
                        }
                    });
                }
            }

            for (let i = 0; i < documentSubjects.length; i++) {
                documentSubjects[i].addEventListener("mouseover", function () {
                    if (documentLinks[i] != null) {
                        documentLinks[i].classList.add("active");
                        documentLinks[i].scrollIntoView();
                        for (let j = 0; j < documentLinks.length; j++) {
                            if (j != i) {
                                documentLinks[j].classList.remove("active");
                            }
                        }
                    }
                });
            }
        }

        let input,
            filter,
            documentTitles,
            listCoursesLink,
            listCoursesItem,
            textValue;
        let isFound = false;
        let minIndexFound = Number.MAX_VALUE;

        document
            .querySelector(".search-bar")
            .addEventListener("keyup", function () {
                input,
                    filter,
                    documentTitles,
                    listCoursesLink,
                    listCoursesItem,
                    textValue;
                isFound = false;
                minIndexFound = Number.MAX_VALUE;
                input = document.querySelector(".search-bar");
                filter = removeVietnameseTones(input.value.toUpperCase());
                listCoursesLink = document.querySelectorAll(
                    ".list-courses-link"
                );
                if (document.querySelectorAll(".list-courses-item") != null)
                    listCoursesItem = document.querySelectorAll(
                        ".list-courses-item"
                    );
                for (let i = 0; i < listCoursesLink.length; i++) {
                    textValue =
                        removeVietnameseTones(listCoursesLink[i].innerText) ||
                        removeVietnameseTones(listCoursesLink[i].textContent);
                    if (textValue.toUpperCase().indexOf(filter) > -1) {
                        if (listCoursesItem[i] != null)
                            listCoursesItem[i].style.display = "";
                        if (i < minIndexFound) minIndexFound = i;
                        isFound = true;
                    } else {
                        if (listCoursesItem[i] != null)
                            listCoursesItem[i].style.display = "none";
                    }
                }
                if (listCoursesItem[minIndexFound] != null)
                    listCoursesItem[minIndexFound].scrollIntoView();
            });

        document
            .querySelector(".search-bar")
            .addEventListener("keypress", function (e) {
                if (e.keyCode === 13) {
                    if (!isFound) {
                        //alert('Không tìm thấy tài nguyên phù hợp!')
                        if (
                            confirm(
                                "Không tìm thấy tài nguyên phù hợp! Bạn có muốn tiếp tục nhập thông tin tìm kiếm không ?"
                            )
                        ) {
                        } else {
                            input.value = "";
                            isFound = false;
                            for (let i = 0; i < listCoursesLink.length; i++) {
                                listCoursesItem[i].style.display = "";
                            }
                        }
                    } else {
                        alert(
                            `Tìm thấy tài nguyên: ${listCoursesItem[minIndexFound].textContent}`
                        );
                        listCoursesItem[minIndexFound].scrollIntoView();
                    }
                }
            });
    }

